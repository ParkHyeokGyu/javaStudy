
public class 문제2 {
	public static void main(String[] args) {
	     int i = 0; 
	     System.out.println("첫번째 결과 : " + i++ * 2); 
	     System.out.println("두번째 결과 : " + i);
	     int j = 0; 
	     System.out.println("첫번째 결과 : " + ++j * 2); 
	     System.out.println("두번째 결과 : " + j);
	}
}
