
public class 문제5 {
	public static void main(String[] args) {
	      int num1=10;   
	      System.out.printf("%d \n", num1);	
	      num1++;
	      System.out.printf("%d \n", num1);	
	      ++num1;
	      System.out.printf("%d \n", num1);	
	      --num1;
	      System.out.printf("%d \n", num1);	
	      num1--;
	      System.out.printf("%d \n", num1);	

	}
}
